"""Reusable runtime for the rendered glue file.

This module holds code for: the long-lived ``LeanRunner`` subprocess
wrapper, ``_coerce``/``_json_default``, telemetry, and the public
``check_all`` / ``check_all_results`` entry points.

The per-domain glue file is generated by
:mod:`generate_glue` and contains a
``GlueConfig`` constructed from ``manifest.json`` and thin shims that
forward into the functions below.

Hypothesis discharge: every Hyp field declared in the manifest is asked
to the SLM (via ``slm_helper.slm_extract``) at most once
per ``check_all_results`` call.  Answers are cached on the
``kwargs['hyp']`` blob the caller can pass in directly to bypass.
"""

