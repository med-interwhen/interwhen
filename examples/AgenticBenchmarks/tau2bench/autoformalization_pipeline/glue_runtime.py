#  LeanRunner — long-lived subprocess
class _Sentinel:
    def __repr__(self) -> str:
        return "<LEAN_UNAVAILABLE>"

_LEAN_UNAVAILABLE = _Sentinel()

def _resolve_binary(explicit: Optional[str]) -> Optional[str]:
    if explicit:
        return explicit
    env = os.environ.get("TAU2_LEAN_BINARY")
    if env:
        return env
    which = shutil.which("policychecker")
    if which:
        return which
    # Look in the standard build directory next to the verifier package.
    here = Path(__file__).resolve().parent
    candidates = [
        here.parent / ".lake" / "build" / "bin" / "policychecker",
        here / ".lake" / "build" / "bin" / "policychecker",
    ]
    for c in candidates:
        if c.exists():
            return str(c)
    return None


class LeanRunner:
    """Manage a single long-running Lean checker process.

    Lazy spawn on first ``query()``, requests serialised by an internal lock,
    auto-restart on death, clean shutdown via ``atexit``.  Drops to
    ``available=False`` on persistent failure and returns ``_LEAN_UNAVAILABLE``
    sentinel thereafter (one warning).
    """

    def __init__(
        self,
        lean_binary_path: Optional[str] = None,
        timeout_s: float = 5.0,
    ) -> None:
        self._binary_path = _resolve_binary(lean_binary_path)
        self._timeout_s = timeout_s
        self._proc: Optional[subprocess.Popen] = None
        self._lock = threading.Lock()
        self._available: bool = True
        self._unavailable_warned: bool = False
        self._query_count: int = 0
        self._deny_count: int = 0
        atexit.register(self.shutdown)

    def _warn_unavailable_once(self, reason: str) -> None:
        if not self._unavailable_warned:
            logger.warning(
                "LeanRunner unavailable (%s); Lean rules will be skipped "
                "for the rest of this process.", reason)
            self._unavailable_warned = True
        self._available = False

    def _spawn(self) -> bool:
        if self._binary_path is None:
            self._warn_unavailable_once("no lean binary path resolved")
            return False
        try:
            self._proc = subprocess.Popen(
                [self._binary_path],
                stdin=subprocess.PIPE,
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
                bufsize=1,
                text=True,
            )
        except (OSError, FileNotFoundError) as e:
            self._warn_unavailable_once(f"failed to spawn: {e!r}")
            self._proc = None
            return False
        logger.warning("LeanRunner spawned policychecker pid=%d at %s",
                       self._proc.pid, self._binary_path)
        return True

    def _ensure_alive(self) -> bool:
        if not self._available:
            return False
        if self._proc is None or self._proc.poll() is not None:
            return self._spawn()
        return True

    def query(self, request: dict) -> Any:
        with self._lock:
            if not self._ensure_alive():
                return _LEAN_UNAVAILABLE
            assert self._proc is not None
            try:
                payload = json.dumps(request, default=_json_default) + "\n"
                self._proc.stdin.write(payload)
                self._proc.stdin.flush()
            except (BrokenPipeError, OSError) as e:
                logger.warning("Lean stdin write failed: %s", e)
                self._kill_proc()
                return _LEAN_UNAVAILABLE
            line = self._readline_with_timeout(self._timeout_s)
            if line is None:
                logger.warning("Lean read timed out (%.2fs); restarting.",
                               self._timeout_s)
                self._kill_proc()
                return _LEAN_UNAVAILABLE
            try:
                resp = json.loads(line)
            except (ValueError, json.JSONDecodeError) as e:
                logger.warning("Lean returned non-JSON %r: %s", line, e)
                return _LEAN_UNAVAILABLE
            if not resp.get("ok", False):
                logger.warning("Lean reported error: %s", resp.get("error"))
                return _LEAN_UNAVAILABLE
            verdict = resp.get("verdict")
            self._query_count += 1
            if verdict is not None:
                self._deny_count += 1
            return None if verdict is None else str(verdict)

    def _readline_with_timeout(self, timeout_s: float) -> Optional[str]:
        result_box: list = []
        def _reader() -> None:
            try:
                line = self._proc.stdout.readline()  # type: ignore[union-attr]
                result_box.append(line)
            except Exception as e:  # pragma: no cover
                result_box.append(e)
        t = threading.Thread(target=_reader, daemon=True)
        t.start()
        t.join(timeout_s)
        if t.is_alive() or not result_box:
            return None
        first = result_box[0]
        return None if isinstance(first, Exception) else first

    def _kill_proc(self) -> None:
        if self._proc is None:
            return
        try: self._proc.kill()
        except Exception: pass
        try: self._proc.wait(timeout=1.0)
        except Exception: pass
        self._proc = None

    def shutdown(self) -> None:
        with self._lock:
            if self._proc is None:
                return
            logger.warning("LeanRunner shutdown: %d total queries, %d denials",
                           self._query_count, self._deny_count)
            try:
                self._proc.stdin.write(json.dumps({"shutdown": True}) + "\n")
                self._proc.stdin.flush()
                self._proc.wait(timeout=1.0)
            except Exception:
                pass
            finally:
                self._kill_proc()


#  JSON coercion
def _json_default(o: Any) -> Any:
    if isinstance(o, Enum):
        return o.value
    if isinstance(o, (date, datetime)):
        return o.isoformat()
    if is_dataclass(o):
        return asdict(o)
    if hasattr(o, "model_dump"):
        try: return o.model_dump()
        except Exception: pass
    if hasattr(o, "dict"):
        try: return o.dict()
        except Exception: pass
    raise TypeError(f"not JSON serialisable: {type(o).__name__}")


def _coerce(value: Any) -> Any:
    if value is None or isinstance(value, (bool, int, float, str)):
        return value
    if isinstance(value, Enum):
        return value.value
    if isinstance(value, (date, datetime)):
        return value.toordinal()
    if isinstance(value, dict):
        return {str(k): _coerce(v) for k, v in value.items()}
    if isinstance(value, (list, tuple, set, frozenset)):
        return [_coerce(v) for v in value]
    if is_dataclass(value):
        return _coerce(asdict(value))
    if hasattr(value, "model_dump"):
        try: return _coerce(value.model_dump())
        except Exception: pass
    if hasattr(value, "dict"):
        try: return _coerce(value.dict())
        except Exception: pass
    if hasattr(value, "__dict__"):
        return _coerce({k: v for k, v in vars(value).items()
                        if not k.startswith("_")})
    return repr(value)


#  Snapshot builder — config-driven

@dataclass
class GlueConfig:
    domain: str
    snapshot_fields: list[tuple]  # (json_key, attr_path, kind) | (..., kind, remap_dict)
    pre_rules: list[dict]
    post_rules: list[dict]
    hyp_questions: dict[str, str] = field(default_factory=dict)
    # Optional: per-list-record field aliases applied to the snapshot.
    # Shape:
    #   { "<json_key>": { "<dest_field>": ["alt1", "alt2"] } }
    # For every record dict under <json_key>, if dest_field is missing
    # but any alt is present, copy it across.
    snapshot_remap: dict = field(default_factory=dict)
    # Optional: extra Python rule callables run after Lean.
    #   pre signature:  (tool_name, tool_args, conversation, db, **kw) -> str|None
    #   post signature: (tool_name, tool_args, result_content, db=None, **kw) -> str|None
    python_pre_rules: list = field(default_factory=list)
    python_post_checks: list = field(default_factory=list)
    # Tools whose PRE rules use a Hyp argument; A Hyp (hypothesis) is an argument derived by asking an SLM a question
    # We propagate the answers in tool_args["hyp"].
    pre_tools_need_hyp: set = field(default_factory=set)


def _attr_chain(obj: Any, path: str) -> Any:
    cur = obj
    for part in path.split("."):
        if cur is None:
            return None
        cur = getattr(cur, part, None)
    return cur


def _today_int() -> int:
    return date.today().toordinal()


def _policy_today_iso() -> str:
    """ISO date string shipped to Lean for fields declared as ``today: String``.

    The Lean side compares dates lexicographically as ISO strings
    (``"YYYY-MM-DD"``), so the snapshot must ship a string in that exact
    format. The value can be pinned via the ``TAU2_POLICY_TODAY`` env var
    (e.g. ``2025-02-25`` for the telecom policy whose header reads
    "The current time is 2025-02-25 12:08:00 EST."); otherwise we fall back
    to the real system date. Shipping an int here would be parsed as ``""``
    by Lean and silently disable every date-aware rule.
    """
    pinned = os.environ.get("TAU2_POLICY_TODAY", "").strip()
    if pinned:
        return pinned
    return date.today().isoformat()


# Float fields the Lean side expects as Nat. Pydantic stores them as floats
# (e.g. 1.5 GB), but Lean's `getNat` returns 0 on a JSON number that isn't an
# integer. Round here so checks like `check_refuelDataAmountValid` see the
# real value rather than 0.
_NAT_RECORD_FIELDS: set[str] = {
    "data_used_gb", "data_refueling_gb", "data_limit_gb",
    "data_limit", "data_used", "data_refueling",
}
_NAT_TOOL_ARG_KEYS: set[str] = {"gb_amount"}


def _coerce_nat(v: Any) -> Any:
    if v is None or isinstance(v, bool):
        return v
    try:
        return max(0, int(round(float(v))))
    except (TypeError, ValueError):
        return v


def _string_pair_list(value: Any) -> list[list[str]]:
    """Return ``[[key, value], ...]`` for state fields like lastToolResults.

    The generated Lean runner parses this representation generically as
    ``List (String × String)``. Dict input is the common runtime shape; list
    input is accepted so callers can pass an already-normalized value.
    """
    if value is None:
        return []
    if isinstance(value, dict):
        return [[str(k), "" if v is None else str(v)] for k, v in value.items()]
    out: list[list[str]] = []
    if isinstance(value, (list, tuple)):
        for item in value:
            if isinstance(item, (list, tuple)) and len(item) == 2:
                out.append([str(item[0]), "" if item[1] is None else str(item[1])])
            elif isinstance(item, dict):
                k = item.get("key", item.get("tool", item.get("name")))
                v = item.get("value", item.get("result", item.get("content")))
                if k is not None:
                    out.append([str(k), "" if v is None else str(v)])
    return out


def _iter_python_rules(registry: Any, tool_name: str) -> list:
    """Support both legacy flat lists and optional per-tool registries."""
    if not registry:
        return []
    if isinstance(registry, dict):
        rules: list = []
        rules.extend(registry.get("*", []) or [])
        rules.extend(registry.get(tool_name, []) or [])
        return rules
    return list(registry)


def _apply_runtime_overrides(db_snap: dict, kwargs: dict) -> None:
    """Overlay runtime/session facts that are not necessarily DB fields."""
    if kwargs.get("user_phone"):
        db_snap["user_phone"] = re.sub(r"\D", "", str(kwargs["user_phone"]))
    if kwargs.get("called_tools") is not None:
        db_snap["called_tools"] = [str(x) for x in (kwargs.get("called_tools") or [])]
    if kwargs.get("last_tool_results") is not None:
        db_snap["last_tool_results"] = _string_pair_list(kwargs.get("last_tool_results"))


def _runtime_fact(kwargs: dict, *keys: str) -> Any:
    for container_name in ("session_facts", "state_facts", "facts"):
        container = kwargs.get(container_name)
        if isinstance(container, dict):
            for key in keys:
                value = container.get(key)
                if value not in (None, ""):
                    return value
    for key in keys:
        value = kwargs.get(key)
        if value not in (None, ""):
            return value
    return None


def _matching_identified_arg(tool_args: Any, db_snap: dict) -> Optional[str]:
    """Find a tool argument matching an ``identified_<entity>`` state field.

    This is a compatibility bridge for generated checkers that encode an
    identification rule as equality to the current argument. The general policy
    shape is: once an entity exists in session state, a same-entity equality
    check may use the corresponding ``<entity>_id`` argument if present.
    """
    if not isinstance(tool_args, dict):
        return None
    for state_key, identified in db_snap.items():
        if not state_key.startswith("identified_") or identified in (None, ""):
            continue
        entity = state_key[len("identified_"):]
        for key in (f"{entity}_id", entity, "id"):
            value = tool_args.get(key)
            if isinstance(value, str) and value:
                return value
    return None


def build_db_snapshot(cfg: GlueConfig, db: Any) -> dict:
    snap: dict = {}
    for entry in cfg.snapshot_fields:
        json_key, attr_path, kind = entry[0], entry[1], entry[2]
        if kind == "date":
            snap[json_key] = _policy_today_iso()
            continue
        if db is None:
            snap[json_key] = [] if kind == "list" else (None if kind == "identified" else "")
            continue
        raw = _attr_chain(db, attr_path)
        if kind == "list":
            snap[json_key] = _coerce(raw or [])
        elif kind == "identified":
            snap[json_key] = raw  # the caller may overwrite
        elif kind == "scalar":
            snap[json_key] = _coerce(raw) if raw is not None else 0
        else:
            snap[json_key] = _coerce(raw) if raw is not None else ""
    # Apply per-record field aliases, e.g. {"customers": {"status": ["account_status"]}}.
    for jkey, aliases in (cfg.snapshot_remap or {}).items():
        items = snap.get(jkey)
        if not isinstance(items, list):
            continue
        for rec in items:
            if not isinstance(rec, dict):
                continue
            for dest, alts in aliases.items():
                if dest in rec and rec[dest] not in (None, ""):
                    continue
                for alt in alts:
                    if alt in rec and rec[alt] not in (None, ""):
                        rec[dest] = rec[alt]
                        break
    # Round float fields that Lean expects as Nat.
    for items in snap.values():
        if not isinstance(items, list):
            continue
        for rec in items:
            if not isinstance(rec, dict):
                continue
            for k in _NAT_RECORD_FIELDS:
                if k in rec:
                    rec[k] = _coerce_nat(rec[k])
    # Normalize phone-like fields to digits-only across all records, so they
    # compare equal to `user_phone` (which is already normalized at call time).
    # Without this, Lean's `line.phoneNumber == state.userPhone` checks always
    # fail because the DB stores "555-123-2002" while user_phone is "5551232002".
    for items in snap.values():
        if not isinstance(items, list):
            continue
        for rec in items:
            if not isinstance(rec, dict):
                continue
            for k, v in list(rec.items()):
                if isinstance(v, str) and "phone" in k.lower():
                    rec[k] = re.sub(r"\D", "", v)
    for k, v in list(snap.items()):
        if isinstance(v, str) and "phone" in k.lower():
            snap[k] = re.sub(r"\D", "", v)
    return snap


#  Result-side representation canonicalization (mirror of DB-side normalization
#  above).  Generated Lean POST rules sometimes compare structured fields from
#  `result_content` against snapshot scalars; without this, "555-123-2002" in
#  the JSON result would fail to equal "5551232002" in `state.userPhone`.
#  Only canonicalizes representation by field-kind heuristic — never decides
#  the policy verdict.

def _normalize_phone_like_fields(value):
    """Recursively normalize keys containing 'phone' to digits-only strings."""
    if isinstance(value, dict):
        return {
            k: (re.sub(r"\D", "", v) if isinstance(v, str) and "phone" in k.lower()
                else _normalize_phone_like_fields(v))
            for k, v in value.items()
        }
    if isinstance(value, list):
        return [_normalize_phone_like_fields(x) for x in value]
    return value


def _normalize_result_content_for_lean(result_content):
    """Canonicalize structured JSON result before shipping to Lean.

    Leaves non-JSON / free-text results untouched.  Returns a JSON string when
    the input is a JSON object/array; returns the original otherwise.
    """
    if not isinstance(result_content, str):
        return result_content
    stripped = result_content.lstrip()
    if not stripped or stripped[0] not in "{[":
        return result_content
    try:
        parsed = json.loads(result_content)
    except (ValueError, TypeError):
        return result_content
    return json.dumps(_normalize_phone_like_fields(parsed), default=_json_default)


#  Identified-customer recovery

_IDENTIFYING_TOOLS = frozenset({
    "get_customer_by_id", "get_customer_by_phone", "get_customer_by_name",
})


def _msg_role(msg: Any) -> Optional[str]:
    return msg.get("role") if isinstance(msg, dict) else getattr(msg, "role", None)


def _msg_content(msg: Any) -> Any:
    return msg.get("content") if isinstance(msg, dict) else getattr(msg, "content", None)


def _msg_tool_calls(msg: Any) -> list:
    if isinstance(msg, dict):
        return msg.get("tool_calls") or []
    return getattr(msg, "tool_calls", None) or []


def _tc_name(tc: Any) -> str:
    if isinstance(tc, dict):
        return tc.get("name") or tc.get("function", {}).get("name", "") or ""
    return getattr(tc, "name", "") or ""


def _infer_identified_customer(conversation: list) -> Optional[str]:
    """Return the customer_id of the most recent successful identifying lookup.

    Domain-agnostic; only fires for the three telecom-style identifying
    tool names but harmless to call on other domains (returns None).
    """
    if not conversation:
        return None
    last_cid: Optional[str] = None
    pending_call: bool = False
    for msg in conversation:
        role = _msg_role(msg)
        if role == "assistant":
            pending_call = False
            for tc in _msg_tool_calls(msg):
                if _tc_name(tc) in _IDENTIFYING_TOOLS:
                    pending_call = True
                    break
            if not pending_call:
                content = _msg_content(msg)
                if isinstance(content, str) and "[Tool call:" in content:
                    for name in _IDENTIFYING_TOOLS:
                        if f"[Tool call: {name}(" in content:
                            pending_call = True
                            break
        elif role == "tool" and pending_call:
            pending_call = False
            content = _msg_content(msg) or ""
            if not isinstance(content, str):
                continue
            try:
                data = json.loads(content)
            except (ValueError, json.JSONDecodeError):
                continue
            if isinstance(data, dict):
                cid = data.get("customer_id")
                if isinstance(cid, str) and cid:
                    last_cid = cid
        else:
            pending_call = False
    return last_cid


#  Ask the SLM each declared hypothesis question
def discharge_hypotheses(
    cfg: GlueConfig,
    conversation: list,
    *,
    overrides: Optional[dict[str, bool]] = None,
) -> dict[str, bool]:
    """Ask the SLM each declared hypothesis question once.

    The caller can supply pre-computed answers via ``overrides`` to skip
    network calls (e.g., when the answer is known at task-classify time).
    Returns a ``{name: bool}`` dict suitable for the ``hyp`` field of a
    Lean POST request.
    """
    overrides = dict(overrides or {})
    if not cfg.hyp_questions:
        return overrides
    answers: dict[str, bool] = {}
    try:
        from slm_helper import slm_extract
    except Exception as e:
        logger.warning("slm_helper unavailable (%s); using overrides only", e)
        return overrides
    for name, question in cfg.hyp_questions.items():
        if name in overrides:
            answers[name] = bool(overrides[name])
            continue
        try:
            raw = slm_extract(
                f"{question} Answer 'yes' or 'no'.",
                conversation,
            )
            answers[name] = (raw or "").strip().lower().startswith("y")
        except Exception as e:
            logger.warning("SLM extraction for hyp %s failed: %s", name, e)
            answers[name] = False
    return answers


#  Telemetry

_STATS_LOCK = threading.Lock()
_STATS_T0 = time.monotonic()
_RULE_STATS: dict[tuple[str, str, str], dict] = {}


def _record_rule(tool: str, rule: str, phase: str, *,
                 fired: bool = False, error: bool = False,
                 unavailable: bool = False, feedback: Optional[str] = None) -> None:
    key = (tool or "<unknown>", rule or "<unknown>", phase or "<unknown>")
    with _STATS_LOCK:
        row = _RULE_STATS.get(key)
        if row is None:
            row = {"queries": 0, "fired": 0, "errors": 0,
                   "unavailable": 0, "last_feedback": None}
            _RULE_STATS[key] = row
        row["queries"] += 1
        if error: row["errors"] += 1
        if unavailable: row["unavailable"] += 1
        if fired:
            row["fired"] += 1
            if feedback:
                row["last_feedback"] = feedback[:500]


def get_rule_stats() -> list[dict]:
    with _STATS_LOCK:
        rows = [{"tool": t, "rule": r, "phase": p, **counts}
                for (t, r, p), counts in _RULE_STATS.items()]
    rows.sort(key=lambda r: (-r["fired"], -r["queries"], r["tool"], r["rule"]))
    return rows


def dump_stats(path: Optional[str] = None) -> Optional[str]:
    rows = get_rule_stats()
    if not rows:
        return None
    if path is None:
        out_dir = os.environ.get("TAU2_VERIFIER_STATS_DIR", "/tmp")
        try: os.makedirs(out_dir, exist_ok=True)
        except OSError: out_dir = "/tmp"
        stamp = datetime.now().strftime("%Y%m%dT%H%M%S")
        path = os.path.join(out_dir, f"verifier_stats_{os.getpid()}_{stamp}.json")
    payload = {"pid": os.getpid(),
               "wall_time_s": round(time.monotonic() - _STATS_T0, 3),
               "stats": rows}
    try:
        with open(path, "w") as f:
            json.dump(payload, f, indent=2)
        logger.warning("verifier rule stats written to %s (%d rules)",
                       path, len(rows))
        return path
    except OSError as e:
        logger.warning("failed to write verifier stats to %s: %s", path, e)
        return None


def _atexit_dump_stats() -> None:
    try: dump_stats()
    except Exception as e:  # pragma: no cover
        logger.warning("verifier stats dump failed: %s", e)


atexit.register(_atexit_dump_stats)

#  Runner singleton

_LEAN_RUNNER: Optional[LeanRunner] = None
_LEAN_RUNNER_LOCK = threading.Lock()


def _get_runner() -> LeanRunner:
    global _LEAN_RUNNER
    if _LEAN_RUNNER is None:
        with _LEAN_RUNNER_LOCK:
            if _LEAN_RUNNER is None:
                _LEAN_RUNNER = LeanRunner()
    return _LEAN_RUNNER


#  Public entrypoints

def _ship(runner: LeanRunner, *, rule: str, tool_args: dict, db_snap: dict,
          tool: str, phase: str, collect: Optional[list[str]] = None) -> Optional[str]:
    try:
        req = {
            "id": uuid4().hex,
            "rule": rule,
            "tool_args": tool_args or {},
            "db_snapshot": db_snap,
        }
        verdict = runner.query(req)
        if verdict is _LEAN_UNAVAILABLE:
            _record_rule(tool, rule, phase, unavailable=True)
            return None
        _record_rule(tool, rule, phase,
                     fired=verdict is not None, feedback=verdict)
        return None if verdict is None else str(verdict)
    except Exception as e:
        logger.warning("Lean rule %s raised %s", rule, e)
        _record_rule(tool, rule, phase, error=True)
        return None


def check_all(cfg: GlueConfig, tool_name, tool_args, conversation, db, **kwargs):
    """Run Lean PRE rules then optional Python PRE rules."""
    failures: list[str] = []
    collect_all = bool(kwargs.get("collect_all"))
    runner = _get_runner()
    db_snap = build_db_snapshot(cfg, db)
    _apply_runtime_overrides(db_snap, kwargs)
    identified = _runtime_fact(kwargs, "identified_customer_id", "identified_customer")
    if identified is None:
        identified = getattr(db, "identified", None) if db is not None else None
    if identified is None:
        identified = _infer_identified_customer(conversation or [])
    if identified is not None:
        db_snap["identified_customer"] = identified
        arg_identity = _matching_identified_arg(tool_args, db_snap)
        if arg_identity is not None:
            db_snap["identified_customer"] = arg_identity

    pre_args = dict(tool_args or {})
    # Round float tool-arg values that Lean expects as Nat (e.g. gb_amount).
    for k in _NAT_TOOL_ARG_KEYS:
        if k in pre_args:
            pre_args[k] = _coerce_nat(pre_args[k])
    if tool_name in (cfg.pre_tools_need_hyp or set()):
        pre_args["hyp"] = discharge_hypotheses(
            cfg, conversation or [], overrides=kwargs.get("hyp_overrides"))

    for spec in cfg.pre_rules:
        if spec["tool"] != tool_name:
            continue
        verdict = _ship(runner, rule=spec["rule"], tool_args=pre_args,
                        db_snap=db_snap, tool=tool_name, phase="pre")
        if verdict is None:
            continue
        if collect_all:
            failures.append(verdict)
        else:
            return verdict

    for rule in _iter_python_rules(cfg.python_pre_rules, tool_name):
        try:
            r = rule(tool_name, tool_args, conversation, db, **kwargs)
        except Exception as e:
            logger.warning("python pre-rule %s raised %s", getattr(rule, "__name__", rule), e)
            _record_rule(tool_name, getattr(rule, "__name__", "<py>"), "pre_py", error=True)
            continue
        _record_rule(tool_name, getattr(rule, "__name__", "<py>"), "pre_py",
                     fired=r is not None, feedback=r if isinstance(r, str) else None)
        if r is not None:
            if collect_all:
                failures.append(r)
            else:
                return r

    return failures or None


def check_all_results(cfg: GlueConfig, tool_name, tool_args, result_content,
                      db=None, **kwargs):
    warnings: list[str] = []
    runner = _get_runner()
    db_snap = build_db_snapshot(cfg, db)
    _apply_runtime_overrides(db_snap, kwargs)
    identified = _runtime_fact(kwargs, "identified_customer_id", "identified_customer")
    if identified is not None:
        db_snap["identified_customer"] = identified
    elif db_snap.get("identified_customer") is None:
        # Same fast path as PRE: prefer the runtime DB attribute, then fall
        # back to conversation inference (which can miss on truncated traces).
        fast = getattr(db, "identified", None) if db is not None else None
        if fast is None:
            fast = _infer_identified_customer(kwargs.get("conversation") or [])
        if fast is not None:
            db_snap["identified_customer"] = fast
    if db_snap.get("identified_customer") is not None:
        arg_identity = _matching_identified_arg(tool_args, db_snap)
        if arg_identity is not None:
            db_snap["identified_customer"] = arg_identity
    post_specs = [spec for spec in cfg.post_rules if spec["tool"] == tool_name]
    needs_hyp = any(spec.get("needs_hyp", bool(cfg.hyp_questions)) for spec in post_specs)
    hyp = discharge_hypotheses(
        cfg, kwargs.get("conversation") or [],
        overrides=kwargs.get("hyp_overrides"),
    ) if needs_hyp else dict(kwargs.get("hyp_overrides") or {})
    post_args = {"result": _normalize_result_content_for_lean(result_content) or "", "hyp": hyp}
    for spec in post_specs:
        verdict = _ship(runner, rule=spec["rule"], tool_args=post_args,
                        db_snap=db_snap, tool=tool_name, phase="post")
        if verdict is not None:
            warnings.append(verdict)

    for chk in _iter_python_rules(cfg.python_post_checks, tool_name):
        try:
            w = chk(tool_name, tool_args, result_content, db=db, **kwargs)
        except Exception as e:
            logger.warning("python post-check %s raised %s", getattr(chk, "__name__", chk), e)
            _record_rule(tool_name, getattr(chk, "__name__", "<py>"), "post_py", error=True)
            continue
        _record_rule(tool_name, getattr(chk, "__name__", "<py>"), "post_py",
                     fired=w is not None, feedback=w if isinstance(w, str) else None)
        if w is not None:
            warnings.append(w)

    return warnings


